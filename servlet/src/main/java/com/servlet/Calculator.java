package com.servlet;

public class Calculator {
    public int somme(int a, int b) {
        return a+b;
    }

    public int multiplier(int a, int b) {
        return a*b;
    }

    public double division(int a, int b) {
        return a/b;
    }

    public double puissance(int a, int b) {
        return a^b;
    }
}
